package menghuanxianjing.mhxj.model;

public class CDKModel {

	private String redeem_name;
	private int redeem_id;
	private int expire_days;
	private int code_redeem_cnt;
	private int gift_id;
	private int amount;
	public String getRedeem_name() {
		return redeem_name;
	}
	public void setRedeem_name(String redeem_name) {
		this.redeem_name = redeem_name;
	}
	public int getRedeem_id() {
		return redeem_id;
	}
	public void setRedeem_id(int redeem_id) {
		this.redeem_id = redeem_id;
	}
	public int getExpire_days() {
		return expire_days;
	}
	public void setExpire_days(int expire_days) {
		this.expire_days = expire_days;
	}
	public int getCode_redeem_cnt() {
		return code_redeem_cnt;
	}
	public void setCode_redeem_cnt(int code_redeem_cnt) {
		this.code_redeem_cnt = code_redeem_cnt;
	}
	public int getGift_id() {
		return gift_id;
	}
	public void setGift_id(int gift_id) {
		this.gift_id = gift_id;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	
}
